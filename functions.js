function myFunction() {
    if (document.getElementById("Choices").value = "Edit") {
        window.location.href = 'edit.html';
        return;
    }
    if (document.getElementById("Choices").value = "Add") {
        window.location.href = 'edit.html';
        return;
    }
    if (document.getElementById("Choices").value = "Delete") {
        window.location.href = 'edit.html';
        return;
    }
}